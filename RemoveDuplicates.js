function removeDuplicates(num) {
  var x,
      len=num.length,
      out=[],
      obj={};
 
  for (x=0; x<len; x++) {
    obj[num[x]]=0;
  }
  for (x in obj) {
    out.push(x);
  }
  return out;
}

var cars = ["Saab", "Saab", "Saab", "Volvo", "BMW", "BMW"];
var uniqueCars = removeDuplicates(cars);
alert(uniqueCars);

